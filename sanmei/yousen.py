# -------------------------------------------------------
# メイン判定ロジック
# -------------------------------------------------------

def calc_yousen(day_kan: str, year_kan: str, month_kan: str, 
                year_shi: str, month_shi: str, day_shi: str,
                year_zokan: str, month_zokan: str, day_zokan: str) -> dict:
    """
    十大主星と十二大従星を算出して辞書形式で返す。
    末尾に「星」を付加して出力する。
    """
    
    # 4.1 十大主星の検索ロジック
    judai_shusei = {
        "north":  JUDAI_SHUSEI_MASTER[day_kan][year_kan] + "星",
        "center": JUDAI_SHUSEI_MASTER[day_kan][month_zokan] + "星",
        "south":  JUDAI_SHUSEI_MASTER[day_kan][month_kan] + "星",
        "east":   JUDAI_SHUSEI_MASTER[day_kan][year_zokan] + "星",
        "west":   JUDAI_SHUSEI_MASTER[day_kan][day_zokan] + "星"
    }

    # 4.2 十二大従星の検索ロジック
    junidai_jusei = {
        "hatsunen": JUNIDAI_JUSEI_MASTER[day_kan][year_shi] + "星",
        "chuunen":  JUNIDAI_JUSEI_MASTER[day_kan][month_shi] + "星",
        "bannen":   JUNIDAI_JUSEI_MASTER[day_kan][day_shi] + "星"
    }

    return {
        "judai_shusei": judai_shusei,
        "junidai_jusei": junidai_jusei
    }
